public class DivisibleByFive {
  public static void main(String[] args) {
      // Loop through numbers 1 to 50
      for (int i = 1; i <= 15; i++) {
          // Check if the number is divisible by 5
          if (i % 5 == 0) {
              System.out.println(i); // Print the number if divisible by 5
          }
      }
  }
}
