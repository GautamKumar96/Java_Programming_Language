public class relational_Operators {
  public static void main(String[]args){
    int a = 50;
    int b = 20;
    System.out.println("a: " + a + "b: " +  b);
    System.out.println("a==b:"+(a==b));
    System.out.println("a!=b:"+(a!=b));
    System.out.println("a>b:"+(a>b));
    System.out.println("a<b:"+(a<b));
    System.out.println("a>=b:"+(a>=b));
    System.out.println("a<=b:"+(a<=b));
    System.out.println("");
  }
}
