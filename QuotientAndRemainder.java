public class QuotientAndRemainder {
    public static void main(String[] args) {
        // i) dividend = 72, divisor = 5
        int dividend1 = 72;
        int divisor1 = 5;
        int quotient1 = dividend1 / divisor1;
        int remainder1 = dividend1 % divisor1;

        System.out.println("For dividend = 72 and divisor = 5:");
        System.out.println("Quotient = " + quotient1);
        System.out.println("Remainder = " + remainder1);
        System.out.println();

        // ii) dividend = 345, divisor = 10
        int dividend2 = 345;
        int divisor2 = 10;
        int quotient2 = dividend2 / divisor2;
        int remainder2 = dividend2 % divisor2;

        System.out.println("For dividend = 345 and divisor = 10:");
        System.out.println("Quotient = " + quotient2);
        System.out.println("Remainder = " + remainder2);
        System.out.println();

        // iii) dividend = 345, divisor = 100
        int dividend3 = 345;
        int divisor3 = 100;
        int quotient3 = dividend3 / divisor3;
        int remainder3 = dividend3 % divisor3;

        System.out.println("For dividend = 345 and divisor = 100:");
        System.out.println("Quotient = " + quotient3);
        System.out.println("Remainder = " + remainder3);
    }
}
